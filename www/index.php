<h2>How to use this dev environment</h2>
<p>This dev environment ist very easy to use:</p>

<ul>
<li>Webfiles: Put everything related to your web files into the <code>www</code>-directory. You can delete the 3 test files which are shipped.</li>
<li>Webserver ports are <code>HTTP -> 80</code> and <code>HTTPS -> 443</code></li>
<li>MySQL-database: Connect to the database with any client to <code>localhost</code> on default port <code>3306</code>. A default database <code>test</code> is installed during setup, you can safely delete this one later.</li>
<li>MySQL root user: Username: <code>root</code> Password: <code>dev</code></li>
<li>MySQL normal user: Username: <code>dev</code> Password: <code>dev</code></li>
</ul>

